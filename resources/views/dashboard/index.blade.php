    <script>
        $(document).ready(function() {
            $('#tambahUser').on('click', function(event) {
                event.preventDefault(); // Mencegah link default behavior

                $.ajax({
                    url: '{{ route("createUser") }}', // URL API
                    method: 'GET',
                    success: function(response) {
                        // Menampilkan data di popup
                        $('#popupUsername').text(response['data']['username']);
                        $('#popupPassword').text(response['data']['password']);
                        $('#popupEmail').text(response['data']['email']);
                        $('#popup').show();

                        $('#dataTable').DataTable().ajax.reload(null, false);
                    },
                    error: function(xhr) {
                        $('#popupMessage').text('Failed to fetch data.');
                        $('#popup').show();
                    }
                });
            });

            $('#closePopup').on('click', function() {
                $('#popup').hide();
            });
        });
    </script>
